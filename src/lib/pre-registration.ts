export function mergePreRegistrationSettings(
  base: PreRegistrationSettings,
  incoming: Partial<PreRegistrationSettings> | null | undefined,
) {
  if (!incoming) {
    return base;
  }

  return {
    ...base,
    ...incoming,
  };
}


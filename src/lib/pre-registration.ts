export function getPreRegistrationInputTypeLabel(inputType: PreRegistrationFieldRecord["inputType"]) {
  switch (inputType) {
    case "textarea":
      return "Uzun metin";
    case "date":
      return "Tarih";
    case "select":
      return "Secimli liste";
    case "email":
      return "E-posta";
    case "phone":
      return "Telefon";
    case "file":
      return "Dosya / Gorsel";
    default:
      return "Kisa metin";
  }
}
